package com.revature.exception;

public class ReimbursementResolvedException extends Exception {

	public ReimbursementResolvedException() {
		super();
		
	}

	public ReimbursementResolvedException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public ReimbursementResolvedException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public ReimbursementResolvedException(String message) {
		super(message);
		
	}

	public ReimbursementResolvedException(Throwable cause) { 
		super(cause);
		
	}

}
